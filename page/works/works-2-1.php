    </div>
    <div class="block">
        <p class="text">
            <span>
                由美國好萊塢20世紀福斯電影公司出資，金獎導演李安執導的3D電影「Life Of Pi少年Pi的奇幻漂流」，
                片中制造船難、漂流海景的造浪池就設在台中水湳經貿園區，並於2012年11月21日全球正式上映前1日開放給媒體議員等體驗驚濤駭浪。
            </span><br><br>
            <span>
                片中的拍片主場景造浪池完全由本公司規劃、設計、建造完成，自2010年5~9月于台中市舊水湳機場勘察，
                最終決定設置於舊航站正前方後開始規劃設計，並於2010年10月5日舉辦動土典禮，
                造浪機組於2011年1月10日開始安裝，於2011年1月31日測試完成2月1日開始造浪，
                並於2月下旬提交李安導演拍片，劇組於2011年5月底拍攝完成返回美國進行後製。
            </span><br><br>
            <span>
                造浪池長75公尺，寬30公尺，均深3公尺，總蓄水量為6750噸(m3)，為全世界第3大拍片用水池，
                第1大造浪池，造浪機組由12部各為150HP的造浪機所組成，每部造浪機設有空氣壓力推出閥門
                (pressure)及真空吸力閥門(vaccum)，每個閥門並具備有7段可調整出力大小，
                因此其幾何變化所組成的浪型是非常的多，經由電腦程式控制每個閥門的開與閉來造出不同浪型，
                小平浪、中浪、大浪，最高可造出3公尺的浪高，而所造出的浪為大海中的浪(ocean wave)，
                與一般水上樂園僅由空氣壓力推出所造出的沙灘浪有所不同。
            </span><br><br>
            <span>
                目前程式內的8種浪型乃經由近百種程式測試後由李安導演所挑選出適合拍片需求的浪型，
                雖僅8種浪型但搭配24個閥門每個閥門有7段出力變化下，仍然可以呈現出無限可能的組合，
                足供日後其他團隊的拍片需求。
            </span><br>
            <span>
                本公司很榮幸有機會參與金獎導演李安及國際級好萊塢團隊的拍片作業，
                雖然偌大的造浪池建造時程極短看似為不可能任務，
                但本公司上下同仁秉持著台灣人的刻苦精神最多曾經排3班接力施作，
                最終努力達成使命，也讓國際級團隊對台灣人另眼相看。
            </span>
        </p>
