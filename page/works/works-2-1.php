            <h6 class="text bold">恭賀第85屆奧斯卡金像獎頒獎，李安導演的Life Of Pi 《少年Pi的奇幻漂流》</h6>
            <h5 class="text bold">榮獲最佳導演、最佳攝影、最佳視覺效果、最佳原創配樂四大獎項。</h5> 
